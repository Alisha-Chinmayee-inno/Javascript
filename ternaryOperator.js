//  Ternary Operator for Conditional Assignment:
let age = 20;
let isAdult = age >= 18 ? "Yes" : "No";

console.log("Is the person an adult? " + isAdult);


// Ternary Operator for Conditional Execution:
let hasPermission = true;

hasPermission ? console.log("You have permission.") : console.log("You don't have permission.");
