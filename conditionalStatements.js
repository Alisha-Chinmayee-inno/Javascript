//  if
let x = 10;

if (x > 5) {
  console.log("x is greater than 5");
}

//  if..else
let age = 18;

if (age >= 18) {
  console.log("You are an adult");
} else {
  console.log("You are not an adult");
}


// if..else..if..else
let grade = 85;

if (grade >= 90) {
  console.log("A");
} else if (grade >= 80) {
  console.log("B");
} else if (grade >= 70) {
  console.log("C");
} else {
  console.log("D");
}
