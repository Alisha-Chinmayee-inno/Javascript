//  >
let a = 5;
let b = 3;

if (a > b) {
  console.log("a is greater than b");
}

// <
let x = 8;
let y = 12;

if (x < y) {
  console.log("x is less than y");
}

//  >=
let num1 = 10;
let num2 = 10;

if (num1 >= num2) {
  console.log("num1 is greater than or equal to num2");
}

//  <=
let age = 16;
let legalAge = 18;

if (age <= legalAge) {
  console.log("You are under or equal to the legal age");
}

//  ==
let value1 = 5;
let value2 = "5";

if (value1 == value2) {
  console.log("The values are equal (loose equality)");
}

//   '!='
let numA = 10;
let numB = 15;

if (numA != numB) {
  console.log("numA is not equal to numB");
}

//  ===
let number1 = 42;
let number2 = "42";

if (number1 === number2) {
  console.log("The values are strictly equal");
}

//  '!=='
let val1 = 1;
let val2 = true;

if (val1 !== val2) {
  console.log("The values are not strictly equal");
}

